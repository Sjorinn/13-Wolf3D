#include <stdlib.h>
#include "../includes/libft.h"

char	*ft_strjoin_free(char *s1, char *s2)
{
	char	*str;

	str = ft_strjoin(s1, s2);
	free(s1);
	s1 = NULL;
	free(s2);
	s2 = NULL;
	return (str);
}
